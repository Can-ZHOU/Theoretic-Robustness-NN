def RE(list1, basedLow, basedUp):
    re = []
    for i in list1:
        if i < basedLow:
            re.append((i-basedLow)/basedLow*100)
        elif i > basedUp:
            re.append((i-basedUp)/basedUp*100)
        else:
            re.append(0)
    print(re)

